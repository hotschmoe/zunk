const std = @import("std");
const wa = @import("wasm_analyze.zig");
const resolver = @import("js_resolve.zig");

pub const GenOptions = struct {
    public_url: []const u8 = "/",
    wasm_filename: []const u8,
    bridge_js: ?[]const u8 = null,
    js_filename: []const u8 = "app.js",
    wasm_preload: bool = false,
    js_integrity: ?[]const u8 = null,
};

pub const Categories = std.enums.EnumSet(resolver.Category);

pub const GenResult = struct {
    js: []const u8,
    html: []const u8,
    report: []const u8,
    js_hash: [16]u8,
    categories: Categories,

    pub fn deinit(self: *GenResult, allocator: std.mem.Allocator) void {
        allocator.free(self.js);
        allocator.free(self.html);
        allocator.free(self.report);
    }
};

pub fn generate(
    allocator: std.mem.Allocator,
    analysis: *const wa.Analysis,
    opts: GenOptions,
) !GenResult {
    var resolutions = try allocator.alloc(resolver.Resolution, analysis.imports.len);
    defer {
        for (resolutions) |*r| allocator.free(r.js_body);
        allocator.free(resolutions);
    }

    var needs = Features{};
    var categories_used = Categories.initEmpty();
    var stub_count: usize = 0;

    for (analysis.imports, 0..) |*imp, i| {
        const sig = analysis.getImportSignature(imp);
        resolutions[i] = try resolver.resolve(allocator, imp, sig);
        const r = &resolutions[i];

        if (r.needs_handles) needs.handles = true;
        if (r.needs_string_helper) needs.strings = true;
        if (r.needs_callbacks) needs.callbacks = true;
        if (r.needs_memory_view) needs.memory_view = true;
        categories_used.insert(r.category);
        if (r.confidence == .stub) stub_count += 1;
    }

    const has_init = analysis.hasExport("init");
    const has_frame = analysis.hasExport("frame");
    const has_resize = analysis.hasExport("resize");
    const has_cleanup = analysis.hasExport("cleanup");

    if (has_frame) needs.render_loop = true;
    if (has_resize) needs.resize_handler = true;
    if (categories_used.contains(.input)) needs.input_system = true;
    if (categories_used.contains(.audio)) needs.audio_state = true;
    if (categories_used.contains(.webgpu)) needs.webgpu_init = true;
    if (categories_used.contains(.ui)) needs.ui_system = true;

    var js: std.ArrayList(u8) = .empty;
    defer js.deinit(allocator);
    const w = js.writer(allocator);

    try w.writeAll("// Generated by zunk -- do not edit.\n");
    try w.writeAll("// https://github.com/hotschmoe/zunk\n\n");

    try w.writeAll("(async function() {\n'use strict';\n\n");

    if (needs.handles) try emitHandleTable(w);
    if (needs.strings) try emitStringHelper(w);
    if (needs.audio_state) try emitAudioState(w);
    if (needs.input_system) try emitInputSystem(w);
    if (needs.webgpu_init) try emitWebGPUState(w);
    if (categories_used.contains(.fetch)) try w.writeAll("let zunkFetchBuf = null;\n\n");
    if (needs.ui_system) try emitUISystem(w);

    try w.writeAll("const env = {\n");

    for (analysis.imports, 0..) |*imp, i| {
        const r = &resolutions[i];

        if (!std.mem.eql(u8, imp.module, "env")) continue;
        try w.print("  {s}() {{ {s} }},\n", .{ imp.name, r.js_body });
    }

    try w.writeAll("};\n\n");

    if (opts.bridge_js) |bridge| {
        try w.writeAll("// --- bridge.js (library-provided) ---\n");
        try w.writeAll(bridge);
        try w.writeAll("\n\n");
    }

    try w.print(
        \\// --- Load WASM ---
        \\const importObject = {{ env }};
        \\const response = await fetch('{s}{s}');
        \\const {{ instance }} = await WebAssembly.instantiateStreaming(response, importObject);
        \\const exports = instance.exports;
        \\const memory = exports.memory;
        \\
        \\
    , .{ opts.public_url, opts.wasm_filename });

    if (needs.strings) {
        try w.writeAll("readStr = (ptr, len) => new TextDecoder().decode(new Uint8Array(memory.buffer, ptr, len));\n\n");
    }

    if (needs.handles) {
        try w.writeAll("H._exports = exports;\n");
        try w.writeAll("H._memory = memory;\n\n");
    }

    if (needs.webgpu_init) try emitWebGPUInit(w);

    if (has_init) {
        try w.writeAll("// Init\nexports.init();\n\n");
    }

    if (needs.render_loop) {
        try w.writeAll(
            \\// --- Render loop ---
            \\let zunkLastTime = 0;
            \\let zunkFrameId = 0;
            \\function zunkFrame(time) {
            \\  const dt = (time - zunkLastTime) / 1000;
            \\  zunkLastTime = time;
            \\
        );
        // Input flush is WASM-driven via input.poll() to avoid wiping per-frame state early.
        try w.writeAll(
            \\  exports.frame(Math.min(dt, 0.1));
            \\  zunkFrameId = requestAnimationFrame(zunkFrame);
            \\}
            \\zunkFrameId = requestAnimationFrame(zunkFrame);
            \\
            \\
        );
    }

    if (needs.resize_handler) {
        if (needs.webgpu_init) {
            try w.writeAll(
                \\// --- Resize handler (DPR-aware for WebGPU) ---
                \\function zunkResize() {
                \\  const c = document.getElementById('app') || document.querySelector('canvas');
                \\  if (c) {
                \\    const dpr = window.devicePixelRatio || 1;
                \\    c.width = Math.round(c.clientWidth * dpr);
                \\    c.height = Math.round(c.clientHeight * dpr);
                \\  }
                \\  exports.resize(c ? c.width : window.innerWidth, c ? c.height : window.innerHeight);
                \\}
                \\window.addEventListener('resize', zunkResize);
                \\(function zunkWatchDPR(){matchMedia(`(resolution:${window.devicePixelRatio}dppx)`).addEventListener('change',()=>{zunkResize();zunkWatchDPR();},{once:true})})();
                \\zunkResize();
                \\
                \\
            );
        } else {
            try w.writeAll(
                \\// --- Resize handler ---
                \\function zunkResize() {
                \\  const c = document.getElementById('app') || document.querySelector('canvas');
                \\  if (c) { c.width = window.innerWidth; c.height = window.innerHeight; }
                \\  exports.resize(window.innerWidth, window.innerHeight);
                \\}
                \\window.addEventListener('resize', zunkResize);
                \\zunkResize();
                \\
                \\
            );
        }
    }

    if (has_cleanup) {
        try w.writeAll("window.addEventListener('beforeunload', () => exports.cleanup());\n\n");
    }

    try w.writeAll(
        \\// --- Global bindings ---
        \\window.wasmBindings = exports;
        \\window.wasmMemory = memory;
        \\dispatchEvent(new CustomEvent('ZunkApplicationStarted', { detail: { wasm: instance, memory } }));
        \\
    );

    try w.writeAll("\n})();\n");

    const full_hash = std.hash.XxHash3.hash(0, js.items);
    var fingerprint: [8]u8 = undefined;
    std.mem.writeInt(u64, &fingerprint, full_hash, .big);
    const hex = std.fmt.bytesToHex(fingerprint, .lower);

    const old_header = "// Generated by zunk -- do not edit.\n// https://github.com/hotschmoe/zunk\n\n";
    const new_header = try std.fmt.allocPrint(allocator, "// Generated by zunk [build:{s}] -- do not edit.\n// https://github.com/hotschmoe/zunk\n\n", .{hex[0..8]});
    defer allocator.free(new_header);
    if (std.mem.startsWith(u8, js.items, old_header)) {
        try js.replaceRange(allocator, 0, old_header.len, new_header);
    }

    var html: std.ArrayList(u8) = .empty;
    defer html.deinit(allocator);
    try generateHtml(html.writer(allocator), analysis, opts, categories_used);

    var report: std.ArrayList(u8) = .empty;
    defer report.deinit(allocator);
    try generateReport(report.writer(allocator), analysis, resolutions, stub_count, hex[0..8]);

    return .{
        .js = try js.toOwnedSlice(allocator),
        .html = try html.toOwnedSlice(allocator),
        .report = try report.toOwnedSlice(allocator),
        .js_hash = hex,
        .categories = categories_used,
    };
}

const Features = struct {
    handles: bool = false,
    strings: bool = false,
    callbacks: bool = false,
    memory_view: bool = false,
    render_loop: bool = false,
    resize_handler: bool = false,
    input_system: bool = false,
    audio_state: bool = false,
    webgpu_init: bool = false,
    ui_system: bool = false,
};

fn emitHandleTable(w: anytype) !void {
    try w.writeAll(
        \\// --- Handle table (JS objects referenced by integer ID) ---
        \\const H = {
        \\  _table: new Map(),
        \\  _next: 1,
        \\  store(obj) { const id = this._next++; this._table.set(id, obj); return id; },
        \\  get(id) { return this._table.get(id); },
        \\  set(id, obj) { this._table.set(id, obj); },
        \\  release(id) { this._table.delete(id); },
        \\  nextId() { return this._next++; },
        \\};
        \\
        \\
    );
}

