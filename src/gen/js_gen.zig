fn emitInputSystem(w: anytype) !void {
    try w.writeAll(
        \\// --- Input system (shared memory polling) ---
        \\const zunkInput = {
        \\  ptr: 0, len: 0,
        \\  keysDown: new Set(),
        \\  keysPressed: new Set(),
        \\  keysReleased: new Set(),
        \\  typedChars: [],
        \\  mouseX: 0, mouseY: 0, mouseDx: 0, mouseDy: 0,
        \\  mouseWheel: 0, mouseButtons: 0,
        \\  touches: [],
        \\  init(ptr, len) {
        \\    this.ptr = ptr; this.len = len;
        \\    document.addEventListener('keydown', e => { this.keysDown.add(e.keyCode); this.keysPressed.add(e.keyCode); if(e.key.length===1&&this.typedChars.length<32)this.typedChars.push(e.key.charCodeAt(0)); else if(e.key==='Backspace'&&this.typedChars.length<32)this.typedChars.push(8); else if(e.key==='Enter'&&this.typedChars.length<32)this.typedChars.push(10); e.preventDefault(); });
        \\    document.addEventListener('keyup', e => { this.keysDown.delete(e.keyCode); this.keysReleased.add(e.keyCode); });
        \\    const canvas = document.getElementById('app') || document.querySelector('canvas') || document;
        \\    canvas.addEventListener('mousemove', e => { const sx=canvas.clientWidth?canvas.width/canvas.clientWidth:1,sy=canvas.clientHeight?canvas.height/canvas.clientHeight:1; this.mouseDx+=e.movementX*sx; this.mouseDy+=e.movementY*sy; this.mouseX=(e.offsetX??e.clientX)*sx; this.mouseY=(e.offsetY??e.clientY)*sy; });
        \\    canvas.addEventListener('mousedown', e => { this.mouseButtons |= (1 << e.button); });
        \\    canvas.addEventListener('mouseup', e => { this.mouseButtons &= ~(1 << e.button); });
        \\    canvas.addEventListener('wheel', e => { this.mouseWheel += e.deltaY; e.preventDefault(); }, {passive:false});
        \\    canvas.addEventListener('touchstart', e => { this.touches = Array.from(e.touches); e.preventDefault(); }, {passive:false});
        \\    canvas.addEventListener('touchmove', e => { this.touches = Array.from(e.touches); }, {passive:false});
        \\    canvas.addEventListener('touchend', e => { this.touches = Array.from(e.touches); }, {passive:false});
        \\  },
        \\  flush() {
        \\    if (!this.ptr) return;
        \\    const view = new DataView(memory.buffer, this.ptr, this.len);
        \\    let off = 0;
        \\    // keys_down bitmap (32 bytes)
        \\    const kd = new Uint8Array(32); this.keysDown.forEach(k => { if(k<256) kd[k>>3] |= (1<<(k&7)); }); new Uint8Array(memory.buffer, this.ptr + off, 32).set(kd); off += 32;
        \\    // keys_pressed bitmap
        \\    const kp = new Uint8Array(32); this.keysPressed.forEach(k => { if(k<256) kp[k>>3] |= (1<<(k&7)); }); new Uint8Array(memory.buffer, this.ptr + off, 32).set(kp); off += 32;
        \\    // keys_released bitmap
        \\    const kr = new Uint8Array(32); this.keysReleased.forEach(k => { if(k<256) kr[k>>3] |= (1<<(k&7)); }); new Uint8Array(memory.buffer, this.ptr + off, 32).set(kr); off += 32;
        \\    // Mouse
        \\    view.setFloat32(off, this.mouseX, true); off += 4;
        \\    view.setFloat32(off, this.mouseY, true); off += 4;
        \\    view.setFloat32(off, this.mouseDx, true); off += 4;
        \\    view.setFloat32(off, this.mouseDy, true); off += 4;
        \\    view.setFloat32(off, this.mouseWheel, true); off += 4;
        \\    view.setUint8(off, this.mouseButtons); off += 1;
        \\    // Touch
        \\    view.setUint8(off, Math.min(this.touches.length, 10)); off += 1;
        \\    for (let i = 0; i < 10; i++) { view.setFloat32(off + i*4, this.touches[i]?.clientX || 0, true); } off += 40;
        \\    for (let i = 0; i < 10; i++) { view.setFloat32(off + i*4, this.touches[i]?.clientY || 0, true); } off += 40;
        \\    for (let i = 0; i < 10; i++) { view.setInt32(off + i*4, this.touches[i]?.identifier || 0, true); } off += 40;
        \\    // Viewport
        \\    off += 1 + 16 + 4; // skip gamepad for now
        \\    view.setUint32(off, window.innerWidth, true); off += 4;
        \\    view.setUint32(off, window.innerHeight, true); off += 4;
        \\    view.setFloat32(off, window.devicePixelRatio, true); off += 4;
        \\    view.setUint8(off, document.hasFocus() ? 1 : 0); off += 1;
        \\    // Typed chars buffer (1 byte count + up to 32 bytes)
        \\    const tc = Math.min(this.typedChars.length, 32);
        \\    view.setUint8(off, tc); off += 1;
        \\    for (let i = 0; i < tc; i++) { view.setUint8(off + i, this.typedChars[i]); }
        \\    // Clear per-frame state
        \\    this.keysPressed.clear(); this.keysReleased.clear();
        \\    this.mouseDx = 0; this.mouseDy = 0; this.mouseWheel = 0;
        \\    this.typedChars.length = 0;
        \\  },
        \\  poll() { this.flush(); },
        \\};
        \\
        \\
    );
}

